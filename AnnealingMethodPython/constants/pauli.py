# (i, j) -> (мнимая единица с правильным знаком, индекс результата)
# Индексы: 0=I, 1=X, 2=Y, 3=Z
PAULI_MAP = {
    (1, 2): (1j, 3),    # X*Y = iZ
    (2, 1): (-1j, 3),   # Y*X = -iZ
    (3, 1): (1j, 2),    # Z*X = iY
    (1, 3): (-1j, 2),   # X*Z = -iY
    (2, 3): (1j, 1),    # Y*Z = iX
    (3, 2): (-1j, 1),   # Z*Y = -iX
}
